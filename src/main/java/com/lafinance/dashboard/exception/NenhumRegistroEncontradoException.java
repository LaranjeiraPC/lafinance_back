package com.lafinance.dashboard.exception;

public class NenhumRegistroEncontradoException extends Exception {
    public NenhumRegistroEncontradoException() { super(); }
    public NenhumRegistroEncontradoException(String message) { super(message); }
    public NenhumRegistroEncontradoException(String message, Throwable cause) { super(message, cause); }
    public NenhumRegistroEncontradoException(Throwable cause) { super(cause); }
}